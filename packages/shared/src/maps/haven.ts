export function buildHaven(): BuiltMap {
  return assembleMap(
    "Sanctum",
    buildFromAscii(LAYOUT, {
      floor: Block.Grass,
      wall: Block.Bricks,
      accent: Block.Stone,
      cover: Block.Planks,
    }),
  );
}
