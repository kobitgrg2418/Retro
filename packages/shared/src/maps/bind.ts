  "###...####.......##########.......####...###", // 12 B-short     A-short
  "###...####.......##########.......####...###", // 13 (hookah)    (showers)
  "###.W.####.......##########.......####.U.###", // 14 teleporter landings
  "###...####...T...##########...V...####...###", // 15 teleporter pads
  "###...####.......##########.......####...###", // 16
