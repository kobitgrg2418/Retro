// Teleporters (one-way, wired by assembleMap):
//   T (B-short room)  ->  U (top of A-long): flank from B side to A side
//   V (A-short room)  ->  W (top of B-long): flank from A side to B side

export function buildBind(): BuiltMap {
  return assembleMap(
    "Twingate",
    buildFromAscii(LAYOUT, {
      floor: Block.Sand,
      wall: Block.Sandstone,
      accent: Block.Stone,
      cover: Block.Sandstone,
    }),
  );
}
