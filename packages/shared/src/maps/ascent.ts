export function buildAscent(): BuiltMap {
  return assembleMap(
    "Cascade",
    buildFromAscii(LAYOUT, {
      floor: Block.Stone,
      wall: Block.Sandstone,
      accent: Block.Planks,
      cover: Block.Sandstone,
    }),
  );
}
