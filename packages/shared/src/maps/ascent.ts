// "Cascade" — original layout inspired by Ascent's strategic skeleton:
// a large open mid courtyard that controls access to both sites (catwalk to
// A-short on the west, market to B-main on the east), two main lanes down the
// edges, and a defender backbone corridor connecting the sites behind.

import { Block } from "../map.js";
import { buildFromAscii } from "./builder.js";
import { assembleMap, type BuiltMap } from "./types.js";

